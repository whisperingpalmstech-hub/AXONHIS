// Deep translate function: for each locale, produce a full clone of en.json
// with key-specific translations applied, falling back to en
function deepTranslate(enObj, locale, flatTranslations, prefix = '') {
  const result = {};
  for (const [key, value] of Object.entries(enObj)) {
    const fullKey = prefix ? `${prefix}.${key}` : key;
    if (typeof value === 'object' && value !== null) {
      result[key] = deepTranslate(value, locale, flatTranslations, fullKey);
    } else {
      // Check if we have a specific translation
      result[key] = flatTranslations[fullKey] || value;
    }
  }
  return result;
}

